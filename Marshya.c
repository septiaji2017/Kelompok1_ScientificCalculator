#include "Marshya.h"

float flr(float x)
{
	float result;
	result = floor(x);
	return result;
}

float cl(float x)
{
	float result;	
	result = ceil(x);
	return result;
}

float rnd(float x)
{
	float result;	
	result = round(x);	
	return result;
}

int absolute(int y)
{
	int result;	
	result = abs(y);	
	return result;
}

